# Импорт необходимых модулей Selenium
from selenium import webdriver  # Основной модуль для работы с браузером
from selenium.webdriver.common.by import By  # Класс для стратегий поиска элементов (By.ID, By.NAME и т.д.)


def test_google_search():
    """
    Тест для проверки загрузки главной страницы Google.
    Проверяет, что в заголовке страницы содержится слово "Google".
    """

    # 1. СОЗДАНИЕ ДРАЙВЕРА (открытие браузера)
    # Инициализируем драйвер Chrome - создается новое окно браузера
    driver = webdriver.Chrome()

    # 2. ПЕРЕХОД НА СТРАНИЦУ
    # Открываем главную страницу Google в браузере
    driver.get("https://google.com")

    # 3. ПРОВЕРКА (ASSERTION)
    # Утверждение: проверяем, что в заголовке страницы есть слово "Google"
    # Если условие не выполняется - тест падает с ошибкой AssertionError
    # driver.title - возвращает заголовок текущей страницы (тег <title> в HTML)
    assert "Google" in driver.title

    # 4. ЗАКРЫТИЕ БРАУЗЕРА (очистка ресурсов)
    # Важно всегда закрывать браузер после выполнения теста
    # driver.quit() закрывает все окна браузера и завершает процесс WebDriver
    # Альтернатива: driver.close() - закрывает только текущее окно
    driver.quit()

# ПРИМЕЧАНИЯ ДЛЯ ЗАПОМИНАНИЯ:
# 1. driver.get(url) - переходит по указанному URL
# 2. driver.title - получает заголовок страницы (отображается во вкладке браузера)
# 3. assert условие - если условие ложно, тест считается неудачным
# 4. Всегда вызывайте driver.quit() в конце, иначе процессы браузера останутся в памяти
# 5. Для поиска элементов используйте driver.find_element(By.СТРАТЕГИЯ, "значение")

